       N.SP_ID,
       N.JOB_CODE,
       N.FO_CREATE_DATE,
       C.FO_COMPL_DATE,
       MG.MTR_ID,
       MG.MODEL_CD,
       MG.PHASE,
       MG.FORM_NBR,
       MG.PREM_ID,
       MG.DIV_NAME,
       MG.ADDR_LINE_1,
       MG.ADDR_LINE_2,
       MG.CITY,
       MG.STATE,
       MG.ZIP,
       MG.CUST_CLASS_CD,
       MG.PREM_LATITUDE,
       MG.PREM_LONGITUDE,
       MG.MR_ROUTE_TYPE,
       N.MTR_READ_ROUTE,
       MG.PHYSICAL_RTE,
       MG.SUBAREA,
       MG.OFFICE,
       SK.DISPATCH_GROUP,
       SK.SKILL_1,
       SK.SKILL_2,
       /* JC Work Type*/
    CASE 	   
		/*Mark based on comments*/
		WHEN CHARINDEX('M1315',upper(N.OFFICE_REMARK))>0 THEN '2G'
		WHEN CHARINDEX('SMOC METER CONNECTOR',UPPER(N.OFFICE_REMARK)) >0 OR
		CHARINDEX('SMOC MATCH',UPPER(N.OFFICE_REMARK)) >0 THEN 'Meter Connector'
    /*WHEN CHARINDEX('NON RES DELAY',UPPER(N.OFFICE_REMARK)) >0 THEN 'NRD'*/
		
		/*Mark based on JC*/
    WHEN N.JOB_CODE IN ('HDEM','HDGM','LDEM','LDGM') THEN 'Meter Reading'
		WHEN N.JOB_CODE in ('8430','8432','8620','8622') THEN '7 Day'
		WHEN N.JOB_CODE in ('8480','8482') THEN '30 Day'
			
	WHEN N.JOB_CODE in ('2140','2142','2143','2144','2146','2148','2149','8350','8590','8592') THEN '65 Day E'
      WHEN N.JOB_CODE in ('5235','5236','5237','5238') THEN '65 Day G'
			
            		WHEN CHARINDEX('E7 TO E-TOUA',UPPER(N.OFFICE_REMARK)) >0 THEN 'E7 Default' 
    
          		WHEN N.JOB_CODE in ('2020','2021','2022','2026','2030','2031','2032','2034','2036','2038','2040','2335',
		'2635','2830','2832','2834','2836','2838','2840''5060','5062','5064','5066','5070','5072','5074','5076','5080','5082','5084','5086',
		'5190','5192','5194','5196','5210','5410','5765','7600','2840','5870','5872','5874','5876','5880','5882','5884','5886')  AND CHARINDEX('RATE REFORM',UPPER(N.OFFICE_REMARK)) >0 THEN 'TOU-SM Deployment' 
      
		WHEN N.JOB_CODE in ('2020','2021','2022','2026','2030','2031','2032','2034','2036','2038','2040','2335',
		'2635','2830','2832','2834','2836','2838','2840''5060','5062','5064','5066','5070','5072','5074','5076','5080','5082','5084','5086',
		'5190','5192','5194','5196','5210','5410','5765','7600','2840','5870','5872','5874','5876','5880','5882','5884','5886')  THEN 'SM Deployment' 

 /*CASE READS JC 2380 & 6370 with comments billing verify*/
 WHEN N.JOB_CODE IN ('2380', '6370') AND CHARINDEX('BILLING REQUEST',upper(N.OFFICE_REMARK))>0 THEN 'Case Reads'
		
	
		/*WHEN N.JOB_CODE in ('5239') THEN 'Curb Remediation'*/
		WHEN N.JOB_CODE in ('2833','2831','2835','2837','2839','2841','5896','5894','5890','5892') THEN 'Opt Out'

		WHEN N.JOB_CODE in ('8680','8690','8700','8701') THEN 'R-Test'
		WHEN N.JOB_CODE in ('8310') THEN 'UG Inspection'
		WHEN N.JOB_CODE in ('8230') THEN 'CTM/DA Inspection'
		WHEN N.JOB_CODE in ('8220') THEN 'I-Test (8220)'
    WHEN N.JOB_CODE in ('8750') THEN 'I-Test (8750)'
		WHEN N.JOB_CODE in ('2181','5261') THEN 'Opt Out MM'
		WHEN CHARINDEX('UPM',upper(N.OFFICE_REMARK))>0 THEN 'UPM'
		WHEN N.JOB_CODE in ('2000','2002','2008','2011','2020','2021',
          '2022','2026','2030','2031','2032','2034','2036','2038','2040','5062','5190','5192','5194','5196','5872')
          and RIGHT(N.MTR_READ_ROUTE,2) <> 'SM'  THEN 'Other ERM2SM'
		WHEN N.JOB_CODE in('5260', '5280','2180','2200', '5140', '2110', '2190', '8510') THEN 'Chng Party Read/Verify'
		WHEN N.JOB_CODE in('8240') THEN 'CTI/PT Install'
		
		/*Mark based on PM_SO*/
		WHEN N.JOB_CODE IN ('2350','2351','2352','2353','2354','2355','2356','2357','2358','2359','2360','2361','2390','2391','2392','2450','2451','2452','2453','2454','2455','2456','2457','2458','2459','2460','2461','2500','2501','2502','2503','2504','2505','2506','2507','2508','2509','2510','2511','3150','3170','7250','7251','7260','7261','7270','7271','7280','7281','7290','7291','7300','7301') THEN 'E Start Stop'
		WHEN N.JOB_CODE IN ('2050','2051','2052','2053','2055','2056','2057','2059','2060','2061','5092','5094','5102','5103','5104','5105','7310','7311','7312','7320','7321','7322','7330','7331','7332','7340','7341','7342','7350','7351','7360','7361','7390','7391','7400','7401','7410','7411','7412','7413','7420','7421','7422','7423','7430','7431','7432','7433','8750') THEN 'New Business'

		/*Mark based on MWC*/
		WHEN N.JOB_CODE IN ('2300','2301','2302','2303','2304','2305','2306','2307','2308','2309','2310','2311','2320','2330','2600','2601','2602','2603','2604','2605','2606','2607','2608','2609','2610','2611','2620','2630','3140','3160','3250','5360','5400','5401','5402','5403','5404','5405','5406','5700','5701','5702','5703','5704','5705','5706','5770','6360','7130','7131','7132','7133','7140','7141','7142','7143','7150','7151','7152','7153','7190','7191','7192','7193','7200','7201','7202','7203','7210','7211','7212','7213') THEN 'SONP'
		WHEN N.JOB_CODE IN ('2170','2380','2550','2551','2552','2553','2554','2555','2556','2557','2558','2559','2560','2561','2650','2652','3030','3110','5250','5650','5651','5652','5653','5654','5655','5656','5780','6260','6320','6370','7030','7040','7041','7042','7043','7050','7051','7052','7053','7060','7061','7062','7063') THEN 'Broken Lock'
		/*WHEN MWC = '25' THEN 'Other E  Exch'
		WHEN MWC = 'EY' THEN 'Other E  Maint'
		WHEN MWC = '74' THEN 'Other G  Exch'
		WHEN MWC = 'HY' THEN 'Other G  Maint'*/
		ELSE 'Other' END AS JC_Work_Type,
        CASE 
	WHEN N.JOB_CODE in ('8430','8432','8620','8622') THEN DATEADD(DAY,7,N.FO_CREATE_DATE)
	WHEN N.JOB_CODE in ('8480','8482') THEN DATEADD(DAY,30,N.FO_CREATE_DATE)
    WHEN N.JOB_CODE in ('2140','2142','2143','2144','2146','2148','2149','8350','8590','8592','5235','5236','5237','5238') THEN DATEADD(DAY,65,N.FO_CREATE_DATE)
    ELSE NULL END
    AS DUE_DATE,
    N.OFFICE_REMARK

    
FROM MIB.dbo.MSTR_FAS_ORDERS_NEW N LEFT OUTER JOIN MIB.dbo.MSTR_FAS_ORDERS_CMP C   
        ON (N.FO_ID = C.FO_ID) LEFT OUTER JOIN RIB.dbo.AJPK_MTR_GEO MG ON N.SP_ID = MG.SP_ID left outer join RIB.dbo.AJPK_JC_SKILL SK on N.JOB_CODE = SK.JOB_CODE
       
where C.FO_COMPL_DATE IS NULL AND N.JOB_CODE NOT in ('HDEM', 'LDEM', 'HDGM', 'LDGM') and [N].[FO_CREATE_DATE] >= DATEADD(MONTH,-12, GETDATE())) as tmp